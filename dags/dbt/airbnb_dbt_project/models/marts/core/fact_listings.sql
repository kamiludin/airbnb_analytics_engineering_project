    SELECT 
        listing_id,
        accommodates,
        bathrooms,
        bedrooms,
        beds,
        price_new,
        minimum_nights,
	    maximum_nights,
	    minimum_minimum_nights,
	    maximum_minimum_nights,
	    minimum_maximum_nights,
	    maximum_maximum_nights,
	    minimum_nights_avg_ntm,
	    maximum_nights_avg_ntm,
	    availability_30,
	    availability_60,
	    availability_90,
	    availability_365,
